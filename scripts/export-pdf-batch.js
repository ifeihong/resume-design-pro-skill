/**
 * Resume Design Pro - Batch PDF Export Script
 * 
 * Usage:
 *   node export-pdf-batch.js <file-or-pattern> [--output-dir <dir>] [--style <style>]
 * 
 * Examples:
 *   # Export all HTML files in current directory
 *   node export-pdf-batch.js *.html
 * 
 *   # Export specific files
 *   node export-pdf-batch.js resume-liming-cyberpunk.html resume-liming-minimalism.html
 * 
 *   # Export all files to a specific directory
 *   node export-pdf-batch.js *.html --output-dir ./pdf-output
 * 
 *   # Export with custom header/footer
 *   node export-pdf-batch.js *.html --header "Resume Design Pro" --footer "Confidential"
 */

const { chromium } = require('playwright');
const fs = require('fs');
const path = require('path');

// Parse command line arguments
function parseArgs(args) {
  const files = [];
  const options = {
    outputDir: null,
    header: '',
    footer: '',
    noBackground: false,
    help: false
  };

  for (let i = 0; i < args.length; i++) {
    const arg = args[i];
    if (arg === '--output-dir' || arg === '-o') {
      options.outputDir = args[++i];
    } else if (arg === '--header' || arg === '-H') {
      options.header = args[++i];
    } else if (arg === '--footer' || arg === '-F') {
      options.footer = args[++i];
    } else if (arg === '--no-background') {
      options.noBackground = true;
    } else if (arg === '--help' || arg === '-h') {
      options.help = true;
    } else if (arg.endsWith('.html') || arg.endsWith('.htm')) {
      files.push(arg);
    }
  }

  return { files, options };
}

