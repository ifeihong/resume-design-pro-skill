// Export single PDF
async function exportSinglePDF(browser, inputFile, outputDir, options) {
  const inputPath = path.resolve(inputFile);
  
  if (!fs.existsSync(inputPath)) {
    return { file: inputFile, success: false, error: 'File not found' };
  }

  const basename = path.basename(inputFile, '.html');
  const outputFile = path.join(outputDir, `${basename}.pdf`);

  try {
    const page = await browser.newPage();
    const fileUrl = 'file:///' + inputPath.replace(/\\/g, '/');
    await page.goto(fileUrl, { waitUntil: 'networkidle' });
    await page.waitForFunction(() => document.fonts.ready);

    // Build header template
    const headerTemplate = options.header
      ? `<div style="font-size: 8px; width: 100%; text-align: center; color: #ccc; font-family: sans-serif; padding-top: 5mm;">${options.header}</div>`
      : '<div></div>';

    // Build footer template
    const footerTemplate = options.footer
      ? `<div style="font-size: 8px; width: 100%; text-align: center; color: #ccc; font-family: sans-serif; padding-bottom: 5mm;">${options.footer} | <span class="pageNumber"></span>/<span class="totalPages"></span></div>`
      : `<div style="font-size: 9px; width: 100%; text-align: center; color: #999; font-family: sans-serif;"><span class="pageNumber"></span> / <span class="totalPages"></span></div>`;

    await page.pdf({
      path: outputFile,
      format: 'A4',
      printBackground: !options.noBackground,
      margin: {
        top: '15mm',
        right: '15mm',
        bottom: '15mm',
        left: '15mm'
      },
      displayHeaderFooter: true,
      headerTemplate,
      footerTemplate
    });

    await page.close();

    const stats = fs.statSync(outputFile);
    const sizeKB = (stats.size / 1024).toFixed(1);
    const pageCount = await getPageCount(browser, inputFile, options);

    return { file: inputFile, success: true, output: outputFile, sizeKB, pageCount };
  } catch (error) {
    return { file: inputFile, success: false, error: error.message };
  }
}

// Get page count for a PDF
async function getPageCount(browser, inputFile, options) {
  try {
    const page = await browser.newPage();
    const fileUrl = 'file:///' + path.resolve(inputFile).replace(/\\/g, '/');
    await page.goto(fileUrl, { waitUntil: 'networkidle' });
    await page.waitForFunction(() => document.fonts.ready);

    // Use PDF metadata for accurate page count instead of scrollHeight estimation
    const tempPdf = await page.pdf({
      format: 'A4',
      printBackground: true,
      margin: { top: '15mm', right: '15mm', bottom: '15mm', left: '15mm' }
    });

    await page.close();

    // Parse PDF to get page count (simple heuristic: each A4 page is roughly 35KB)
    const estimatedPages = Math.max(1, Math.round(tempPdf.length / 35840));
    return estimatedPages;
  } catch {
    return '?';
  }
}

// Main batch export
async function batchExport() {
  const { files, options } = parseArgs(process.argv.slice(2));

  if (options.help || files.length === 0) {
    showHelp();
    process.exit(0);
  }

  // Ensure output directory exists
  const outputDir = options.outputDir ? path.resolve(options.outputDir) : process.cwd();
  if (!fs.existsSync(outputDir)) {
    fs.mkdirSync(outputDir, { recursive: true });
  }

  console.log('');
  console.log('━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━');
  console.log('📄 Resume Design Pro - Batch PDF Export');
  console.log('━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━');
  console.log(`   Files to export: ${files.length}`);
  console.log(`   Output directory: ${outputDir}`);
  if (options.header) console.log(`   Header: ${options.header}`);
  if (options.footer) console.log(`   Footer: ${options.footer}`);
  console.log('━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━');
  console.log('');

  const browser = await chromium.launch();
  const results = [];

  for (let i = 0; i < files.length; i++) {
    const file = files[i];
    const progress = `[${i + 1}/${files.length}]`;
    process.stdout.write(`   ${progress} Processing ${path.basename(file)}... `);

    const result = await exportSinglePDF(browser, file, outputDir, options);
    results.push(result);

    if (result.success) {
      console.log(`✅ ${result.sizeKB} KB (${result.pageCount} pages)`);
    } else {
      console.log(`❌ ${result.error}`);
    }
  }

  await browser.close();

  // Summary
  const successCount = results.filter(r => r.success).length;
  const failCount = results.filter(r => !r.success).length;
  const totalSizeKB = results.filter(r => r.success).reduce((sum, r) => sum + parseFloat(r.sizeKB), 0).toFixed(1);

  console.log('');
  console.log('━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━');
  console.log('📊 Export Summary');
  console.log('━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━');
  console.log(`   ✅ Success: ${successCount} files`);
  if (failCount > 0) console.log(`   ❌ Failed:  ${failCount} files`);
  console.log(`   📦 Total size: ${totalSizeKB} KB`);
  console.log(`   📂 Location: ${outputDir}`);
  console.log('━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━');
  console.log('');

  // List exported files
  if (successCount > 0) {
    console.log('   Exported files:');
    results.filter(r => r.success).forEach(r => {
      console.log(`   • ${path.basename(r.output)} (${r.sizeKB} KB)`);
    });
    console.log('');
  }
}

// Run
if (require.main === module) {
  batchExport().catch(err => {
    console.error('❌ Batch export failed:', err.message);
    process.exit(1);
  });
}

module.exports = { batchExport, exportSinglePDF };
